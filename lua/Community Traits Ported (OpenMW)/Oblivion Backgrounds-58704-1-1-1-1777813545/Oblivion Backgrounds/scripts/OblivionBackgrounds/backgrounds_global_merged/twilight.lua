local resteredPlayers = {}

local function checkAltar(obj, actor)
    if actor.type ~= types.Player or not resteredPlayers[actor.id] then return end
    return not blockedShrines[obj.recordId]
end

return {
    eventHandlers = {
        OblivionBackgrounds_registerTwilight = function(playerId)
            if not next(resteredPlayers) then
                I.Activation.addHandlerForType(types.Activator, checkAltar)
            end
            resteredPlayers[playerId] = true
        end
    }
}
